class Profile {
    constructor(name, surname, stars, messages, houseDirection) {
        this.name = name,
        this.surname = surname,
        this.stars = stars,
        this.messages = messages,
        this.houseDirection = houseDirection
    }
}

class Travel {
    constructor(origin, destination, driver, timeTravel) {
        this.origin = origin,
        this.destination = destination,
        this.driver = driver,
        this.timeTravel = timeTravel
    }
}